# 제출용 긴 파일명과 현재 보유 데이터셋의 짧은 파일명을 모두 지원한다.
# 긴 파일명 예: 20220417_000442_1_WLINE_01_04_PROD_001_01_LB.csv
FILE_RE = re.compile(
    r"(?P<date>\d{8})_(?P<time>\d{6})_(?P<seq>\d+)_"
    r"(?P<line>[A-Za-z0-9]+)_(?P<batch>\d{2})_(?P<product_id>[A-Za-z0-9_-]+)_"
    r"(?P<lead_num>\d{2})_(?:(?:CH(?P<channel>[01]))|(?P<laser_id>L[AB]))\.csv$"
)
SIMPLE_FILE_RE = re.compile(
    r"(?P<date>\d{8})_battery_(?P<battery_id>\d+)_"
    r"(?:(?:CH(?P<channel>[01]))|(?P<laser>laser_[ab]))\.csv$"
)
# internal_mapping.md 기준: MASKED_CH은 laser_b/LB, MASKED_CH은 laser_a/LA이다.
LASER_NAME_TO_CHANNEL = {"laser_b": 0, "laser_a": 1}
LASER_ID_TO_CHANNEL = {"LB": 0, "LA": 1}
# internal_mapping.md 기준:
#   MASKED_CH = 반사광(Reflected) = LaserB = channel 0
#   MASKED_CH = 출사광(Emitted)   = LaserA = channel 1
# concat_out_0       : 출사광(LaserA) CSV 묶음 → channel 1
# concat_reflected_1 : 반사광(LaserB) CSV 묶음 → channel 0
CHANNEL_FOLDER_TO_CHANNEL = {
    "concat_out_0": 1,
    "concat_reflected_1": 0,
    "reflect": 0,
    "laser_b": 0,
    "out": 1,
    "laser_a": 1,
}
DATE_RE = re.compile(r"^\d{8}$")
TRAILING_CHANNEL_RE = re.compile(r"_(?:CH[01]|laser_[ab]|L[AB])$", re.IGNORECASE)
TRAILING_DIGITS_RE = re.compile(r"(\d+)$")


def parse_event_time(date_text: str, time_text: str) -> datetime:
    return datetime.strptime(
        f"{date_text}{time_text}", "%Y%m%d%H%M%S"
    ).replace(tzinfo=timezone.utc)


def isoformat_z(dt: datetime) -> str:
    return dt.astimezone(timezone.utc).isoformat().replace("+00:00", "Z")


def lead_type_from_num(lead_num: int) -> str:
    return "AL_CU" if lead_num % 2 == 1 else "CU_CU"


def channel_code(channel: int) -> str:
    return "LB" if channel == 0 else "LA"


def channel_name(channel: int) -> str:
    return "LaserB" if channel == 0 else "LaserA"


@dataclass
class ProductRecord:
    """One physical product instance produced by one welding line."""

    # ProductRecord는 스캔 단계에서 만든 "원본 제품 1개"의 메타데이터와 CSV 경로 묶음이다.
    product_instance_id: str
    product_id: str
    line_id: str
    batch_id: str
    sequence_id: str
    event_time: datetime
    # files[channel][lead_num] = CSV path 형태로 저장해 두면 발행 단계에서 채널 순서대로 꺼낼 수 있다.
    files: dict[int, dict[int, Path]] = field(
        default_factory=lambda: {0: {}, 1: {}}
    )

    def add_file(self, lead_num: int, channel: int, path: Path) -> None:
        self.files[channel][lead_num] = path

    @property
    def leads_present(self) -> set[int]:
        return set(self.files[0].keys()) | set(self.files[1].keys())

    @property
    def channel_pairs(self) -> int:
        return len(set(self.files[0].keys()) & set(self.files[1].keys()))

    @property
    def is_complete(self) -> bool:
        return self.channel_pairs >= 16


@dataclass
class PublishItem:
    # PublishItem은 실제 Kafka로 보낼 1개 제품 이벤트이다.
    # 같은 원본 CSV도 line_id나 product_instance_id를 바꿔 여러 생산라인 데이터처럼 replay할 수 있다.
    record: ProductRecord
    product_instance_id: str
    line_id: str
    line_number: int
    event_time: datetime
    is_duplicate: bool = False
    original_product_instance_id: str | None = None
    replay_iteration: int = 0


def build_instance_id(parts: dict[str, str]) -> str:
    return (
        f"{parts['date']}_{parts['time']}_{parts['seq']}_"
        f"{parts['line']}_{parts['batch']}_{parts['product_id']}"
    )


def infer_date_from_path(root: Path, csv_path: Path) -> str | None:
    for path_part in csv_path.relative_to(root).parts:
        if DATE_RE.match(path_part):
            return path_part
    return None


def infer_channel_from_path(root: Path, csv_path: Path) -> int | None:
    for path_part in csv_path.relative_to(root).parts:
        channel = CHANNEL_FOLDER_TO_CHANNEL.get(path_part.lower())
        if channel is not None:
            return channel
    return None


def product_id_from_stem(stem: str) -> str:
    return TRAILING_CHANNEL_RE.sub("", stem)


def line_number_from_line_id(line_id: str) -> int:
    matched = TRAILING_DIGITS_RE.search(line_id or "")
    if matched:
        return int(matched.group(1))
    return 1


def scan_data_dir(data_dir: str) -> list[ProductRecord]:
    """Scan CSV files and group them into line/product instances."""
    root = Path(data_dir)
    if not root.exists():
        raise FileNotFoundError(f"data directory does not exist: {data_dir}")

    records: dict[str, ProductRecord] = {}
    ignored = 0

    for csv_path in sorted(root.glob("**/*.csv")):
        # 1순위: 제출 문서의 정식 파일명 규칙을 파싱한다.
        folder_channel = infer_channel_from_path(root, csv_path)
        match = FILE_RE.match(csv_path.name)
        if match:
            parts = match.groupdict()
            instance_id = build_instance_id(parts)
            lead_num = int(parts["lead_num"])
            channel = (
                LASER_ID_TO_CHANNEL[parts["laser_id"]]
                if parts.get("laser_id")
                else int(parts["channel"])
            )
            if folder_channel is not None:
                channel = folder_channel
            product_id = parts["product_id"]
            line_id = parts["line"]
            batch_id = parts["batch"]
            sequence_id = parts["seq"]
            event_time = parse_event_time(parts["date"], parts["time"])
        else:
            # 2순위: 현재 데이터셋의 짧은 파일명 규칙을 파싱한다.
            # 예: 20220417_battery_10_laser_b.csv
            simple_match = SIMPLE_FILE_RE.match(csv_path.name)
            if simple_match:
                parts = simple_match.groupdict()
                channel = (
                    LASER_NAME_TO_CHANNEL[parts["laser"].lower()]
                    if parts.get("laser")
                    else int(parts["channel"])
                )
                if folder_channel is not None:
                    channel = folder_channel

                lead_num = 1
                product_id = f"battery_{int(parts['battery_id'])}"
                # 현재 샘플 데이터는 라인 정보가 파일명에 없으므로 기본 라인으로 시작한다.
                # --line-count 옵션을 쓰면 발행 단계에서 LINE_01, LINE_02처럼 확장된다.
                line_id = "LINE_01"
                batch_id = parts["date"]
                sequence_id = parts["battery_id"]
                instance_id = f"{parts['date']}_{product_id}"
                event_time = parse_event_time(parts["date"], "000000")
            else:
                # 3순위: DATA_DIR\{date}\out|reflect\*.csv 구조를 지원한다.
                # 폴더명이 채널을 알려주면 파일명은 제품 id로 사용한다.
                data_date = infer_date_from_path(root, csv_path)
                if folder_channel is None or data_date is None:
                    ignored += 1
                    continue

                channel = folder_channel
                lead_num = 1
                product_id = product_id_from_stem(csv_path.stem)
                line_id = "LINE_01"
                batch_id = data_date
                sequence_id = product_id
                instance_id = f"{data_date}_{product_id}"
                event_time = parse_event_time(data_date, "000000")

        if instance_id not in records:
            # 같은 product_instance_id 아래에 laser_b와 laser_a CSV를 계속 모은다.
            records[instance_id] = ProductRecord(
                product_instance_id=instance_id,
                product_id=product_id,
                line_id=line_id,
                batch_id=batch_id,
                sequence_id=sequence_id,
                event_time=event_time,
            )

        records[instance_id].add_file(lead_num, channel, csv_path)

    result = sorted(records.values(), key=lambda r: (r.event_time, r.line_id, r.product_instance_id))
    logger.info(
        "Scanned %s product instances from %s. complete=%s ignored_files=%s",
        len(result),
        data_dir,
        sum(1 for r in result if r.is_complete),
        ignored,
    )
    return result


def load_signal(path: Path) -> np.ndarray:
    """
    Load one CSV signal as float32.

    The expected format is one numeric column. If multiple numeric columns are
    present, the last column is used because exported measurement files often
    include an index/time column before the signal column.
    """
    data = np.genfromtxt(str(path), delimiter=",", dtype=np.float32)
    if data.size == 0:
        raise ValueError("empty signal file")

    if data.ndim == 2:
        data = data[:, -1]

    data = np.asarray(data, dtype=np.float32).reshape(-1)
    data = data[np.isfinite(data)]
    if data.size == 0:
        raise ValueError("no finite numeric samples")
    return data


def make_message(
    item: PublishItem,
    lead_num: int,
    channel: int,
    file_path: Path,
    samples: np.ndarray,
    chunk_index: int,
    total_chunks: int,
    chunk_size: int | None = None,
    chunk_start: int | None = None,
    chunk_end: int | None = None,
) -> dict:
    if chunk_start is None:
        if chunk_size is None:
            raise ValueError("chunk_size is required when chunk_start is not provided")
        chunk_start = chunk_index * chunk_size
    if chunk_end is None:
        if chunk_size is None:
            raise ValueError("chunk_size is required when chunk_end is not provided")
        chunk_end = min(chunk_start + chunk_size, len(samples))

    start = int(chunk_start)
    end = int(chunk_end)
    chunk_values = np.asarray(samples[start:end], dtype=np.float32)
    chunk_checksum = hashlib.sha1(chunk_values.tobytes()).hexdigest()
    chan_code = channel_code(channel)
    # message_id는 Consumer/DB에서 중복 저장을 막기 위한 결정론적 id이다.
    message_id = (
        f"{item.product_instance_id}:L{lead_num:02d}:"
        f"{chan_code}:{chunk_index:06d}"
    )
    prefixed_file_name = f"{item.line_number}_{file_path.name}"

    return {
        "message_id": message_id,
        "product_instance_id": item.product_instance_id,
        "product_id": item.record.product_id,
        "line_id": item.line_id,
        "batch_id": item.record.batch_id,
        "sequence_id": item.record.sequence_id,
        "lead_num": lead_num,
        "lead_type": lead_type_from_num(lead_num),
        "channel": channel,
        "channel_name": channel_name(channel),
        "chunk_index": chunk_index,
        "total_chunks": total_chunks,
        "is_last_chunk": chunk_index == total_chunks - 1,
        "sample_rate_hz": SAMPLE_RATE_HZ,
        "start_sample": start,
        "end_sample": end,
        "samples": chunk_values.tolist(),
        "event_time": isoformat_z(item.event_time),
        "metadata": {
            "source": "file_replay_producer",
            "version": PRODUCER_VERSION,
            "file_name": prefixed_file_name,
            "original_product_instance_id": item.original_product_instance_id
            or item.record.product_instance_id,
            "is_duplicate": item.is_duplicate,
            "replay_iteration": item.replay_iteration,
            "chunk_checksum": chunk_checksum,
        },
    }


def _json_token_size(sample: float) -> int:
    """Return utf-8 byte size for one JSON float token."""
    return len(
        json.dumps(float(sample), ensure_ascii=False, separators=(",", ":")).encode(
            "utf-8"
        )
    )


def split_signal_into_byte_chunks(
    signal: np.ndarray, target_chunk_bytes: int
) -> list[tuple[int, int]]:
    """
    Split signal into variable-size chunks based on approximate JSON payload bytes.

    The estimate follows JSON array representation: `[s1,s2,...]`.
    """
    if target_chunk_bytes <= 64:
        raise ValueError("target_chunk_bytes must be > 64")
    if len(signal) == 0:
        return []

    ranges: list[tuple[int, int]] = []
    chunk_start = 0
    current_bytes = 2  # '[' + ']'
    current_count = 0

    for idx, value in enumerate(signal):
        token_bytes = _json_token_size(float(value))
        add_bytes = token_bytes + (1 if current_count > 0 else 0)

        if current_count > 0 and (current_bytes + add_bytes) > target_chunk_bytes:
            ranges.append((chunk_start, idx))
            chunk_start = idx
            current_bytes = 2 + token_bytes
            current_count = 1
        else:
            current_bytes += add_bytes
            current_count += 1

    if current_count > 0:
        ranges.append((chunk_start, len(signal)))

    if len(ranges) > MAX_CHUNKS_PER_SIGNAL:
        merged: list[tuple[int, int]] = []
        merge_step = int(math.ceil(len(ranges) / MAX_CHUNKS_PER_SIGNAL))
        for i in range(0, len(ranges), merge_step):
            start = ranges[i][0]
            end = ranges[min(i + merge_step - 1, len(ranges) - 1)][1]
            merged.append((start, end))
        ranges = merged

    return ranges


def build_producer(kafka_bootstrap: str, retries: int = 8) -> KafkaProducer:
    """Create KafkaProducer with at-least-once delivery settings."""
    last_error: Exception | None = None

    for attempt in range(1, retries + 1):
        try:
            producer = KafkaProducer(
                bootstrap_servers=kafka_bootstrap,
                # acks=all은 broker가 저장 확인을 줄 때까지 성공 처리하지 않아 유실 가능성을 낮춘다.
                acks="all",
                # 일시적인 broker/network 문제는 Producer가 자동 재시도한다.
                retries=10,
                retry_backoff_ms=500,
                request_timeout_ms=30000,
                # 같은 연결에서 여러 요청이 앞질러 가는 것을 막아 chunk 순서 설명이 쉬워진다.
                max_in_flight_requests_per_connection=1,
                compression_type=os.getenv("KAFKA_COMPRESSION", "gzip"),
                linger_ms=20,
                batch_size=65536,
                max_request_size=MAX_REQUEST_SIZE,
                key_serializer=lambda value: value.encode("utf-8"),
                value_serializer=lambda value: json.dumps(
                    value, ensure_ascii=False, separators=(",", ":")
                ).encode("utf-8"),
            )
            logger.info("Connected to Kafka bootstrap=%s", kafka_bootstrap)
            return producer
        except NoBrokersAvailable as exc:
            last_error = exc
            logger.warning(
                "Kafka is not ready. attempt=%s/%s bootstrap=%s",
                attempt,
                retries,
                kafka_bootstrap,
            )
            time.sleep(3)

    raise RuntimeError(f"failed to connect to Kafka: {last_error}")


