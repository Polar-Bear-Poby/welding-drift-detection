def make_publish_items(
    records: list[ProductRecord],
    target_products: int,
    max_products: int | None,
    line_count: int,
    line_interval_seconds: float,
    line_seeds: list[int] | None = None,
) -> list[PublishItem]:
    """Create line-aware replay items for demo/load-test scenarios.

    line_seeds: 각 생산라인이 제품을 전송하는 순서를 결정하는 random seed 목록.
                길이가 line_count보다 짧으면 순환 사용한다.
                None이면 원래 정렬 순서 그대로 사용한다.
    """
    selected = records[:max_products] if max_products else records
    if not selected:
        return []

    if line_count < 1:
        raise ValueError("line_count must be >= 1")

    # 라인별로 제품 순서를 셔플한다 (seed가 주어진 경우).
    per_line_records: list[list[ProductRecord]] = []
    for line_index in range(line_count):
        shuffled = list(selected)
        if line_seeds:
            seed = line_seeds[line_index % len(line_seeds)]
            rng = random.Random(seed)
            rng.shuffle(shuffled)
        per_line_records.append(shuffled)

    total_products = target_products if target_products > 0 else len(selected) * line_count
    items: list[PublishItem] = []
    base_event_time = selected[0].event_time

    for index in range(total_products):
        line_index = index % line_count
        line_number = line_index + 1
        product_index = index // line_count
        line_pool = per_line_records[line_index]
        source = line_pool[product_index % len(line_pool)]
        line_id = source.line_id if line_count == 1 else f"LINE_{line_index + 1:02d}"
        if line_count == 1:
            line_number = line_number_from_line_id(line_id)
        is_replay = product_index >= len(line_pool) or line_id != source.line_id
        replay_no = product_index // len(line_pool)
        product_instance_id = (
            source.product_instance_id
            if not is_replay
            else f"{source.product_instance_id}_{line_id}_R{replay_no:05d}"
        )

        items.append(
            PublishItem(
                record=source,
                product_instance_id=product_instance_id,
                line_id=line_id if is_replay else source.line_id,
                line_number=line_number,
                event_time=base_event_time
                + timedelta(seconds=product_index * line_interval_seconds),
                is_duplicate=is_replay,
                original_product_instance_id=source.product_instance_id,
                replay_iteration=replay_no,
            )
        )

    return sorted(items, key=lambda item: (item.event_time, item.line_id, item.product_instance_id))


def publish_product(
    producer: KafkaProducer,
    item: PublishItem,
    topic: str,
    topic_laser_a: str | None,
    topic_laser_b: str | None,
    chunk_size: int,
    target_chunk_bytes: int | None,
    speed: float,
) -> int:
    """Publish all available leads/channels for one product instance."""
    sent = 0
    record = item.record
    topic_by_channel = {
        0: topic_laser_b or topic,
        1: topic_laser_a or topic,
    }

    for lead_num in sorted(record.leads_present):
        # 한 제품 안에서 laser_b(channel 0), laser_a(channel 1)를 순서대로 발행한다.
        for channel in (0, 1):
            file_path = record.files[channel].get(lead_num)
            if file_path is None:
                logger.warning(
                    "Missing channel. product=%s lead=%s channel=%s",
                    item.product_instance_id,
                    lead_num,
                    channel,
                )
                continue

            try:
                signal = load_signal(file_path)
            except Exception as exc:
                logger.error("Failed to load %s: %s", file_path, exc)
                continue

            _ = chunk_size  # backward compatibility for CLI argument.
            target_bytes = target_chunk_bytes or TARGET_CHUNK_BYTES
            chunk_ranges = split_signal_into_byte_chunks(
                signal, target_chunk_bytes=target_bytes
            )
            if not chunk_ranges:
                logger.warning(
                    "No chunk generated. product=%s lead=%s channel=%s",
                    item.product_instance_id,
                    lead_num,
                    channel,
                )
                continue
            total_chunks = len(chunk_ranges)
            chan_code = channel_code(channel)
            # partition key에 line/product/lead/laser를 넣어 같은 신호의 chunk가 같은 파티션에 쌓이게 한다.
            partition_key = (
                f"{item.line_id}_{item.product_instance_id}_"
                f"L{lead_num:02d}_{chan_code}"
            )

            for chunk_index, (chunk_start, chunk_end) in enumerate(chunk_ranges):
                # 큰 CSV 전체를 한 메시지로 보내지 않고 chunk로 나눠 broker/message size 부담을 줄인다.
                message = make_message(
                    item=item,
                    lead_num=lead_num,
                    channel=channel,
                    file_path=file_path,
                    samples=signal,
                    chunk_index=chunk_index,
                    total_chunks=total_chunks,
                    chunk_start=chunk_start,
                    chunk_end=chunk_end,
                )
                target_topic = topic_by_channel[channel]
                producer.send(target_topic, key=partition_key, value=message).add_callback(
                    on_send_success
                ).add_errback(on_send_error)
                sent += 1

                if speed > 0:
                    # speed는 "제품 간격"이 아니라 한 제품 내부 신호 chunk의 재생 속도이다.
                    delay_seconds = ((chunk_end - chunk_start) / SAMPLE_RATE_HZ) / speed
                    if delay_seconds >= 0.002:
                        time.sleep(delay_seconds)

    return sent


def run(args: argparse.Namespace) -> int:
    # Set up file logger before proceeding
    setup_file_logger(args.storage_dir)

    # 실행 흐름: 스캔 -> replay 목록 생성 -> dry-run 출력 또는 Kafka 발행.
    records = scan_data_dir(args.data_dir)
    if args.only_complete:
        records = [record for record in records if record.is_complete]
        logger.info("Filtered to complete product instances: %s", len(records))

    # --oldest-date-only: 가장 오래된 event_date의 제품만 선택한다.
    if args.oldest_date_only and records:
        oldest_date = min(r.event_time.date() for r in records)
        records = [r for r in records if r.event_time.date() == oldest_date]
        logger.info(
            "Filtered to oldest date %s: %s product instances", oldest_date, len(records)
        )

    if not records:
        logger.error("No matching product files found.")
        return 2

    # --line-seed: "1,2,3" 형식의 문자열을 정수 리스트로 변환한다.
    line_seeds: list[int] | None = None
    if args.line_seed:
        try:
            line_seeds = [int(s.strip()) for s in args.line_seed.split(",")]
        except ValueError:
            logger.error("--line-seed must be comma-separated integers, e.g. '1,2,3'")
            return 1

    items = make_publish_items(
        records=records,
        target_products=args.target_products,
        max_products=args.max_products,
        line_count=args.line_count,
        line_interval_seconds=args.line_interval_seconds,
        line_seeds=line_seeds,
    )

    if args.dry_run:
        original_count = sum(1 for item in items if not item.is_duplicate)
        duplicate_count = sum(1 for item in items if item.is_duplicate)
        lines = sorted({item.line_id for item in items})
        print("Dry run summary")
        print(f"- data_dir: {args.data_dir}")
        print(f"- storage_dir: {args.storage_dir or '(not configured)'}")
        print(f"- product_instances: {len(items)}")
        print(f"- originals: {original_count}")
        print(f"- duplicates: {duplicate_count}")
        print(f"- lines: {', '.join(lines)}")
        print(f"- line_count: {args.line_count}")
        print(f"- line_interval_seconds: {args.line_interval_seconds}")
        print(f"- topic_laser_a: {args.topic_laser_a or args.topic}")
        print(f"- topic_laser_b: {args.topic_laser_b or args.topic}")
        return 0

    producer = build_producer(args.kafka)

    try:
        while True:
            total_messages = 0
            first_event_time = items[0].event_time
            started_at = time.monotonic()
            for index, item in enumerate(items, start=1):
                if not args.no_schedule_wait:
                    # event_time 차이를 wall-clock 대기로 바꿔
                    # "각 생산라인에서 10초마다 CSV 생성" 상황을 시뮬레이션한다.
                    scheduled_after = (
                        item.event_time - first_event_time
                    ).total_seconds()
                    wait_seconds = scheduled_after - (time.monotonic() - started_at)
                    if wait_seconds > 0:
                        time.sleep(wait_seconds)

                pub_start = time.monotonic()
                sent = publish_product(
                    producer=producer,
                    item=item,
                    topic=args.topic,
                    topic_laser_a=args.topic_laser_a,
                    topic_laser_b=args.topic_laser_b,
                    chunk_size=args.chunk_size,
                    target_chunk_bytes=args.target_chunk_bytes,
                    speed=args.speed,
                )
                pub_elapsed = time.monotonic() - pub_start
                total_messages += sent
                msg_per_sec = sent / pub_elapsed if pub_elapsed > 0 else 0.0

                logger.info(
                    "[%s/%s] product=%s line=%s duplicate=%s messages=%s elapsed_ms=%.2f msg_per_sec=%.2f",
                    index,
                    len(items),
                    item.product_instance_id,
                    item.line_id,
                    item.is_duplicate,
                    sent,
                    pub_elapsed * 1000,
                    msg_per_sec,
                )

            producer.flush()
            total_elapsed = time.monotonic() - started_at
            avg_msg_per_sec = total_messages / total_elapsed if total_elapsed > 0 else 0.0
            
            # flush 이후 종료해야 Python 프로세스가 끝나기 전에 buffered message가 모두 전송된다.
            logger.info(
                "Run complete. total_messages=%s total_elapsed_sec=%.2f avg_msg_per_sec=%.2f", 
                total_messages, total_elapsed, avg_msg_per_sec
            )

            if not args.loop:
                break
            logger.info("Loop mode enabled. Restarting replay in 5 seconds.")
            time.sleep(5)
    except KeyboardInterrupt:
        logger.info("Interrupted by user.")
    finally:
        producer.flush()
        producer.close()
        logger.info("Producer closed.")

    return 0


def parse_args(argv: Iterable[str] | None = None) -> argparse.Namespace:
    # CLI 옵션은 실험 조건을 바꾸는 부분이다. 코드 수정 없이 라인 수/속도/목표 제품 수를 조절한다.
    parser = argparse.ArgumentParser(
        description="Welding CSV to Kafka producer",
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
    )
    parser.add_argument(
        "--data-dir",
        default=DEFAULT_DATA_DIR,
        required=DEFAULT_DATA_DIR is None,
        help="CSV root directory. Defaults to DATA_DIR from .env",
    )
    parser.add_argument(
        "--storage-dir",
        default=DEFAULT_STORAGE_DIR,
        help="Future output/archive root directory. Defaults to STORAGE_DIR from .env",
    )
    parser.add_argument("--kafka", default=DEFAULT_KAFKA, help="Kafka bootstrap server")
    parser.add_argument(
        "--topic",
        default=TOPIC_RAW,
        help="Fallback Kafka topic used when per-channel topics are not set.",
    )
    parser.add_argument(
        "--topic-laser-a",
        default=TOPIC_RAW_LASER_A or None,
        help="Kafka topic for Laser A (channel=1). Defaults to --topic when omitted.",
    )
    parser.add_argument(
        "--topic-laser-b",
        default=TOPIC_RAW_LASER_B or None,
        help="Kafka topic for Laser B (channel=0). Defaults to --topic when omitted.",
    )
    parser.add_argument(
        "--chunk-size",
        type=int,
        default=DEFAULT_CHUNK_SIZE,
        help="Deprecated compatibility option. Chunking uses TARGET_CHUNK_BYTES.",
    )
    parser.add_argument(
        "--target-chunk-bytes",
        type=int,
        default=TARGET_CHUNK_BYTES,
        help="Target JSON payload size (bytes) per chunk.",
    )
    parser.add_argument("--speed", type=float, default=50.0, help="Replay speed multiplier")
    parser.add_argument(
        "--line-count",
        type=int,
        default=1,
        help="Number of production lines to simulate",
    )
    parser.add_argument(
        "--line-interval-seconds",
        type=float,
        default=10.0,
        help="Seconds between products from each production line",
    )
    parser.add_argument(
        "--no-schedule-wait",
        action="store_true",
        help="Do not wait for the simulated line interval; publish as fast as chunk speed allows",
    )
    parser.add_argument(
        "--target-products",
        type=int,
        default=0,
        help="Replay duplicate product instances until this count is reached",
    )
    parser.add_argument(
        "--max-products",
        type=int,
        default=None,
        help="Limit original product instances for a short demo",
    )
    parser.add_argument(
        "--only-complete",
        action="store_true",
        help="Publish only products with 16 Laser A/B lead pairs",
    )
    parser.add_argument("--loop", action="store_true", help="Replay continuously")
    parser.add_argument("--dry-run", action="store_true", help="Scan and print summary only")
    parser.add_argument(
        "--oldest-date-only",
        action="store_true",
        help="Use only product instances from the oldest date folder found in data_dir",
    )
    parser.add_argument(
        "--line-seed",
        default=None,
        help="Comma-separated random seeds for per-line shuffle, e.g. '1,2,3'",
    )
    return parser.parse_args(argv)


if __name__ == "__main__":
    sys.exit(run(parse_args()))

