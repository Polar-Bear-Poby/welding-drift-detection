"""
Kafka producer for welding sensor CSV replay.

Scenario
--------
There is no live sensor API in this assignment. Instead, each production line
is assumed to create one product's CSV files every 10 seconds. This producer
replays those CSV files as if they were newly generated by line PCs/NAS devices,
groups Laser A and Laser B into product instances, splits each signal into
chunks, and publishes the chunks to Kafka topic `welding.raw.v1`.

Run examples
------------
python producer.py --kafka localhost:29092 --speed 50
python producer.py --target-products 2000 --speed 100
python producer.py --dry-run

강사님께 설명할 핵심 흐름
------------------------
1. 실제 센서 API가 없으므로 CSV가 생산라인별로 10초마다 생성된다고 가정한다.
2. data 폴더를 스캔해서 한 제품의 laser_b(MASKED_CH) / laser_a(MASKED_CH) CSV를 묶는다.
3. 큰 신호 배열을 chunk 단위로 나눠 Kafka 메시지 크기를 안정적으로 유지한다.
4. line_id + product_instance_id + lead + laser를 key로 사용해 같은 신호의 순서를 보장한다.
5. --line-count로 생산라인을 늘려도 라인별 key가 분리되므로 Kafka가 병렬 처리할 수 있다.
"""

